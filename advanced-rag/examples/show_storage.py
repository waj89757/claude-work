"""展示 Chroma 存储结构的具体例子"""
import sqlite3
import numpy as np
from pathlib import Path

# 模拟的数据

print("=" * 60)
print("【例子】3 个文档的存储")
print("=" * 60)

# ============================================================
# 原始文档
# ============================================================
documents = [
    {"id": "doc_0", "text": "RAG是检索增强生成技术", "source": "rag.md"},
    {"id": "doc_1", "text": "向量数据库存储embedding", "source": "vector.md"},
    {"id": "doc_2", "text": "BM25是关键词检索算法", "source": "bm25.md"},
]

print("\n📄 原始文档:")
for doc in documents:
    print(f"  {doc['id']}: {doc['text']}")

# ============================================================
# 向量文件里存的内容（简化为4维向量便于展示）
# ============================================================
print("\n" + "=" * 60)
print("📦 向量文件 (data_level0.bin) 里存的内容:")
print("=" * 60)

# 实际是 Embedding 模型生成的，这里模拟
vectors = {
    "doc_0": [0.82, 0.15, -0.33, 0.91],   # RAG 相关
    "doc_1": [0.79, 0.21, -0.28, 0.88],   # 向量相关（和 doc_0 相似）
    "doc_2": [0.12, 0.95, 0.67, -0.15],   # BM25 相关（和前两个不同）
}

print("""
向量文件是二进制格式，内容逻辑上是这样的：

┌─────────┬────────────────────────────────┐
│ 索引    │ 向量 (实际是384或1024维)       │
├─────────┼────────────────────────────────┤
│ 0       │ [0.82, 0.15, -0.33, 0.91]      │  ← doc_0 的向量
│ 1       │ [0.79, 0.21, -0.28, 0.88]      │  ← doc_1 的向量
│ 2       │ [0.12, 0.95, 0.67, -0.15]      │  ← doc_2 的向量
└─────────┴────────────────────────────────┘

注意：向量文件只存向量，不存文档内容！
      索引 0, 1, 2 对应哪个文档，要去 SQLite 查。
""")

# ============================================================
# SQLite 里存的内容
# ============================================================
print("=" * 60)
print("🗄️  SQLite (chroma.sqlite3) 里存的内容:")
print("=" * 60)

print("""
表: embeddings

┌─────────┬──────────────────────────────┬─────────────────────────┐
│ id      │ document                     │ metadata                │
├─────────┼──────────────────────────────┼─────────────────────────┤
│ doc_0   │ "RAG是检索增强生成技术"       │ {"source": "rag.md"}    │
│ doc_1   │ "向量数据库存储embedding"    │ {"source": "vector.md"} │
│ doc_2   │ "BM25是关键词检索算法"        │ {"source": "bm25.md"}   │
└─────────┴──────────────────────────────┴─────────────────────────┘

还有一个映射表，记录 id 和 向量索引 的对应关系：

┌─────────┬──────────────┐
│ id      │ vector_index │
├─────────┼──────────────┤
│ doc_0   │ 0            │
│ doc_1   │ 1            │
│ doc_2   │ 2            │
└─────────┴──────────────┘
""")

# ============================================================
# Query 流程演示
# ============================================================
print("=" * 60)
print("🔍 Query 流程演示: '什么是RAG'")
print("=" * 60)

query = "什么是RAG"
query_vector = [0.80, 0.18, -0.30, 0.89]  # 模拟的 query 向量

print(f"""
Step 1: Query 转向量
  "{query}" → Embedding 模型 → {query_vector}

Step 2: 从向量文件读取所有文档向量，计算相似度
""")

def cosine_sim(a, b):
    a, b = np.array(a), np.array(b)
    return np.dot(a, b) / (np.linalg.norm(a) * np.linalg.norm(b))

print("  ┌──────────────┬────────────────────────────────┬───────────┐")
print("  │ vector_index │ 文档向量                       │ 相似度    │")
print("  ├──────────────┼────────────────────────────────┼───────────┤")
for idx, (doc_id, vec) in enumerate(vectors.items()):
    sim = cosine_sim(query_vector, vec)
    print(f"  │ {idx}            │ {vec} │ {sim:.4f}    │")
print("  └──────────────┴────────────────────────────────┴───────────┘")

print("""
  排序后: vector_index = [0, 1, 2] (按相似度从高到低)
  
Step 3: 用 vector_index 去 SQLite 查文档内容

  SQL: SELECT * FROM embeddings WHERE vector_index IN (0, 1)
  
  结果:
  ┌─────────┬──────────────────────────────┐
  │ id      │ document                     │
  ├─────────┼──────────────────────────────┤
  │ doc_0   │ "RAG是检索增强生成技术"       │  ← 最相关！
  │ doc_1   │ "向量数据库存储embedding"    │  
  └─────────┴──────────────────────────────┘

Step 4: 返回给用户
  [Document("RAG是检索增强生成技术"), Document("向量数据库存储embedding")]
""")

print("=" * 60)
print("✅ 总结")
print("=" * 60)
print("""
向量文件: 存向量数值，用于计算相似度，返回"索引"
SQLite:   存文档内容，根据"索引"查出原文

两者通过 vector_index 关联起来！
""")
